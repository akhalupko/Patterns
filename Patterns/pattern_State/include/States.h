#ifndef STATES_H 
#define STATES_H

#include <Elevator.h>

class Elevator;
//////////////////////////////////////////////////////////////////////////
// ElevatorState 
//////////////////////////////////////////////////////////////////////////
class ElevatorState 
{
public:
	virtual	void		Call( Elevator* elevator, unsigned floor ) = 0;
};

//////////////////////////////////////////////////////////////////////////
// ElevatorIdle
//////////////////////////////////////////////////////////////////////////
class ElevatorIdle : public ElevatorState
{
public:
	virtual void		Call( Elevator* elevator, unsigned floor );
};

//////////////////////////////////////////////////////////////////////////
// ElevatorBusy
//////////////////////////////////////////////////////////////////////////
class ElevatorBusy : public ElevatorState
{
public:
	virtual void		Call( Elevator* elevator, unsigned floor );
};


#endif // STATES_H